 * @method static \Illuminate\Database\Eloquent\Builder<static>|Project withoutTrashed()
 * @mixin \Eloquent
 */
class Project extends Model
{
    use SoftDeletes;

    protected $fillable = [
        'title',
        'description',
        'delivery_date'
    ];

    protected $casts = [
        'delivery_date' => 'date'
    ];

    public function tasks()
    {
        return $this->hasMany(Task::class);
    }
}
